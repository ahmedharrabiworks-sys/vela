// ── Speed clamp ───────────────────────────────────────────────────────────────
// ElevenLabs only accepts speed in [0.7, 1.2].
export function clampSpeed(speed: number): number {
  return Math.min(1.2, Math.max(0.7, speed));
}

// ── Transcriber ───────────────────────────────────────────────────────────────
// "nova-3-general" is confirmed in the DeepgramTranscriber model union (api.d.ts:238).
// It is the general-purpose nova-3 variant; we use it instead of plain "nova-3"
// because Vapi's platform routes "nova-3" (conversational) to nova-2 for multi-language
// calls — "nova-3-general" stays on nova-3 reliably.
// "multi" confirmed in language union (api.d.ts:240).
// endpointing:300 adds minimal latency but prevents missed short words (critical for Arabic).
export function getTranscriberConfig() {
  return {
    provider: "deepgram" as const,
    model: "nova-3-general" as const,
    language: "multi" as const,
    endpointing: 300,
  };
}

// ── Speaking plan (barge-in / interruption) ───────────────────────────────────
// All speech in this codebase uses firstMessageMode:
// "assistant-speaks-first-with-model-generated-message", meaning the opening line
// is generated by GPT-4o (not a canned firstMessage string) and IS fully interruptible.
// stopSpeakingPlan is tuned for near-instant interruption.
export function getSpeakingPlanConfig() {
  return {
    stopSpeakingPlan: {
      numWords: 0,
      voiceSeconds: 0.05,
      backoffSeconds: 0.1,
    },
    startSpeakingPlan: {
      waitSeconds: 0.3,
      smartEndpointingPlan: { provider: "vapi" as const },
    },
  };
}

// ── Shared voice config builder ───────────────────────────────────────────────
